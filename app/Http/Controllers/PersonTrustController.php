    {

        Validator::make($request->all(), [
            'name' => 'required|string',
            'numero' => 'required|string',
            'imageProfil' => 'string'
        ])->validate();
        try {
            PersonTrust::create($request->all());
            return response()->json([
                'success' => "created successfully"
            ], 200);
        } catch (\Throwable $th) {
            return response()->json([
                'fail' => $th
            ], 402);
        }
    }

